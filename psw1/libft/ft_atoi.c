#include "libft.h"

int	ft_atoi(const char *str)
{
	long	i;
	long	nbr;
	int		sing;

	i = 0;
	nbr = 0;
	sing = 0;
	while (str[i] != '\0' && (str[i] == 32 || str[i] == '\t'
			|| str[i] == '\n'
			|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f'))
		i++;
	if (str[i] != '\0' && str[i] == '-')
	{
		sing = 1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] != '\0' && ft_isdigit(str[i]))
		nbr = (nbr * 10) + (str[i++] - '0');
	if (sing == 1)
		return (-nbr);
	return (nbr);
}
